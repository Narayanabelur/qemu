#ifndef QEMU_CONTROL_KEYS_H
#define QEMU_CONTROL_KEYS_H
#define BACKSLASH 0x00005c     /* control input \\  */
#define CTRL      0x000063     /* \c    */
#define ALT       0x000061     /* \a    */
#define SHIFT     0x000071     /* \s  */
#define META      0x00006d     /* \m  */
#define TAB       0x000074     /* \t  */
#define ESC       0x657363     /* \esc */
#define UP        0x000075     /* \u */
#define DOWN      0x000064     /* \d  */
#define LEFT      0x00006c     /* \l */
#define RIGHT     0x000072     /* \r */
#define INSERT    0x000069     /* \i */
#define HOME      0x000068     /* \h */
#define PAGEUP    0x007075     /* \pu */
#define DELETE    0x64656c     /* \del */
#define END       0x656e64     /* \end */
#define PAGEDOWN  0x007064     /* \pd */
#define BACKSPACE 0x006273     /* \bs */
#define ENTER     0x006372     /* \cr */
#define F1        0x006631     /* \f1 */
#define F2        0x006632     /* \f2 */
#define F3        0x006633     /* \f3 */
#define F4        0x006634     /* \f4 */
#define F5        0x006635     /* \f5 */
#define F6        0x006636     /* \f6 */
#define F7        0x006637     /* \f7 */
#define F8        0x006638     /* \f8 */
#define F9        0x006639     /* \f9 */
#define F10       0x663130     /* \fa */
#define F11       0x663131     /* \fb */
#define F12       0x663132     /* \fc */
#endif //QEMU_CONTROL_KEYS_H
